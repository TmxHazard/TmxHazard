public class First_ten_natural_numbers {
    public static void main(String[] args){
        //here 1st we assign i=1 and we tell that the range is upto 10 and we i++ to add +1 to the previous number
        for(int i=1;i<=10;i++){
            System.out.print(i + " ");

        }
       
    }
    
}
